package com.certificate.java.professional.operator;

public class EqualityRelationalOperator {

  public static void main(String[] args) {

    int a = 2, b =5;

    System.out.println("a == b " + (a==b));
    System.out.println("a != b " + (a!=b));
    System.out.println("a > b " + (a>b));
    System.out.println("a < b " + (a<b));
    System.out.println("a <= b " + (a<=b));
    System.out.println("a >= b " + (a>=b));
  }
}
